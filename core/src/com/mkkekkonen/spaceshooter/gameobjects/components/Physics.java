package com.mkkekkonen.spaceshooter.gameobjects.components;

public class Physics {
    private float x;
    private float y;

    public Physics(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }
}
